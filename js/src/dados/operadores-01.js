// Operadores Aritiméticos

// Usando valores literais
console.log(10 + 5);
console.log(10 - 5);
console.log(10 * 5);
console.log(10 / 5);
console.log(10 % 5);

// Usando variáveis
let x = 10;
let y = 5;

console.log(x + y);
console.log(x - y);
console.log(x * y);
console.log(x / y);
console.log(x % y);

// Incremento e Decremento
let z = 10;

console.log(z++); // Primeiro mostrou no console, e depois incrementou 
console.log(++z); // Primeiro incrementou, e depois mostrou no console
console.log(z);

console.log(z--);
console.log(--z);
console.log(z);